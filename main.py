# Crie um sistema onde precisamos verifica a idade do usuario
# se é maior de idade



idade  = int(input('Idade: '))


# verificação
# and - e  or - ou  not - não
# sinais aritméticos  ----  matemática  -  + | - | * |  / | // | % 
# sinais lógicos  ----                  > |  <  |  >= | <= |  == | 
print(idade >= 18)


# True -  1
# False -  0



verifico =  idade >= 18 and 'Maior de idade' or 'Menor de idade'
                  
print(verifico)


